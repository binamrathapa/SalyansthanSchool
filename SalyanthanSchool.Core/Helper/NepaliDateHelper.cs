        // ── Start Reference Point ──────────────────────────
        // Nepali 2000/1/1 = English 1943/4/14
        private static readonly int NepaliStartYear   = 2000;
        private static readonly int EnglishStartYear  = 1943;
        private static readonly int EnglishStartMonth = 4;
        private static readonly int EnglishStartDay   = 14;

        // ── Convert English Date to Nepali ─────────────────
        public static (int Year, int Month, int Day) 
            ToNepali(DateTime englishDate)
        {
            // Total days from reference point
            var refDate = new DateTime(
                EnglishStartYear,
                EnglishStartMonth,
                EnglishStartDay);

            int totalDays = (int)(englishDate.Date - refDate).TotalDays;

            if (totalDays < 0)
                throw new ArgumentException(
                    "Date must be after 1943/4/14");

            int nepYear  = NepaliStartYear;
            int nepMonth = 1;
            int nepDay   = 1;

            // Find nepali year
            int yearIndex = 0;
            while (yearIndex < NepaliYearData.GetLength(0))
            {
                // Total days in this nepali year
                int daysInYear = 0;
                for (int m = 0; m < 12; m++)
                    daysInYear += NepaliYearData[yearIndex, m];

                if (totalDays < daysInYear)
                    break;

                totalDays -= daysInYear;
                nepYear++;
                yearIndex++;
            }

            // Find nepali month
            int monthIndex = 0;
            while (monthIndex < 12)
            {
                int daysInMonth = NepaliYearData[yearIndex, monthIndex];

                if (totalDays < daysInMonth)
                    break;

                totalDays -= daysInMonth;
                nepMonth++;
                monthIndex++;
            }

            // Remaining = day
            nepDay = totalDays + 1;

            return (nepYear, nepMonth, nepDay);
        }

        // ── Get Nepali Month Name (Nepali Script) ──────────
        public static string GetNepaliMonthName(int nepaliMonth)
        {
            if (nepaliMonth < 1 || nepaliMonth > 12)
                return string.Empty;

            return NepaliMonthNames[nepaliMonth - 1];
        }

        // ── Get Nepali Month Name in English ──────────────
        public static string GetNepaliMonthNameEnglish(
            int nepaliMonth)
        {
            if (nepaliMonth < 1 || nepaliMonth > 12)
                return string.Empty;

            return NepaliMonthNamesEnglish[nepaliMonth - 1];
        }

        // ── Get Current Nepali Month Name ──────────────────
        public static string GetCurrentNepaliMonthName()
        {
            var today = DateTime.Now;
            var (_, month, _) = ToNepali(today);
            return GetNepaliMonthName(month);
        }

        // ── Get Nepali Date String ─────────────────────────
        public static string GetNepaliDateString(DateTime date)
        {
            var (year, month, day) = ToNepali(date);
            return $"{year}/{month:D2}/{day:D2}";
        }

        // ── Get Nepali Year ────────────────────────────────
        public static int GetCurrentNepaliYear()
        {
            var (year, _, _) = ToNepali(DateTime.Now);
            return year;
        }

        // ── Get Current Nepali Month Number ───────────────
        public static int GetCurrentNepaliMonth()
        {
            var (_, month, _) = ToNepali(DateTime.Now);
            return month;
        }
